export function validatePhoneNumber(phone: string): boolean {
  // Удаляем все пробелы, скобки, дефисы и другие символы, кроме цифр и +
  const cleanPhone = phone.replace(/[\s\-$$$$]/g, "")

  // Регулярное выражение для проверки телефонных номеров:
  // - Может начинаться с + (опционально)
  // - Затем от 7 до 15 цифр (международный стандарт)
  // - Поддерживает российские номера: 8, 7, +7
  // - Поддерживает международные номера: +375, +380, и т.д.
  const phoneRegex = /^(\+?[1-9]\d{6,14}|8\d{10})$/

  return phoneRegex.test(cleanPhone)
}

export function formatPhoneError(): string {
  return "Введите корректный номер телефона (например: +79119878987, 89099878987, 78987678987)"
}
